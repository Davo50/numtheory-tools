def ring_info(ring_type, form, d):
    """
    Возвращает информацию о кольце.
    ring_type ∈ {'Z', 'Q'}
    form ∈ {'sqrt', 'cbrt'}
    d — целое число
    """
    d = int(d)

    # Q[...] всегда поле
    if ring_type == 'Q':
        # Исключаем d = 1 (тогда Q[√1] = Q — тривиально)
        if d == 1:
            return {
                'found': True,
                'name': 'Q',
                'factorial': True,
                'euclidean': True,
                'norm': 'N(a) = |a| (тривиально)',
                'note': 'Q — поле рациональных чисел, тривиально УФД и евклидово.'
            }
        form_name = 'Q[√' + str(d) + ']' if form == 'sqrt' else 'Q[∛' + str(d) + ']'
        return {
            'found': True,
            'name': form_name,
            'factorial': True,
            'euclidean': True,
            'norm': 'В поле все ненулевые элементы обратимы ⇒ любая функция Δ(r)=0 подходит',
            'note': (f'{form_name} — поле (конечное расширение Q), '
                     'значит тривиально факториально и евклидово. '
                     'Содержательный вопрос — про кольцо ЦЕЛЫХ '
                     f'O_{{{form_name}}} ≈ Z[{"√" if form == "sqrt" else "∛"}{d}], '
                     'см. запись для Z[…].')
        }

    # Z[...] — ищем в базе
    key = (ring_type, form, d)
    if key in RING_DATABASE:
        entry = dict(RING_DATABASE[key])
        entry['found'] = True
        return entry

    # Не нашли — дадим общий ответ по теории
    if form == 'sqrt':
        if d < 0:
            # Мнимые квадратичные
            ufd_set = {-1, -2, -3, -7, -11, -19, -43, -67, -163}
            euclid_set = {-1, -2, -3, -7, -11}
            is_ufd = d in ufd_set
            is_euclid = d in euclid_set
            note = ('Мнимое квадратичное кольцо. По теореме Старка-Хегнера '
                    'УФД только при d ∈ {−1, −2, −3, −7, −11, −19, −43, −67, −163}. '
                    'Из них евклидово (по обычной норме) только {−1, −2, −3, −7, −11}. '
                    f'Для d = {d}: UFD = {is_ufd}, Евклидово = {is_euclid}.')
            return {
                'found': False,
                'name': f'Z[√{d}]',
                'factorial': is_ufd,
                'euclidean': is_euclid,
                'norm': f'N(a + b√{d}) = a² + {abs(d)}b²',
                'note': note,
                'in_database': False
            }
        else:
            # Вещественные
            euclid_set = {2, 3, 5, 6, 7, 11, 13, 17, 19, 21, 29, 33, 37, 41, 57, 73}
            is_euclid = d in euclid_set
            note = ('Вещественное квадратичное кольцо. Евклидово по обычной '
                    'норме |a² − db²| при d ∈ {2, 3, 5, 6, 7, 11, 13, 17, 19, '
                    '21, 29, 33, 37, 41, 57, 73}. '
                    'Факториальных значительно больше — полный список бесконечен и '
                    'неизвестен (проблема Гаусса).')
            return {
                'found': False,
                'name': f'Z[√{d}]',
                'factorial': is_euclid,  # если евклидово, то точно УФД
                'euclidean': is_euclid,
                'norm': f'|N(a + b√{d})| = |a² − {d}b²|',
                'note': note,
                'in_database': False
            }

    # Кубические — общая оговорка
    return {
        'found': False,
        'name': f'Z[∛{d}]',
        'factorial': None,
        'euclidean': None,
        'norm': f'N(a + b∛{d} + c∛{d**2}) = a³ + {d}b³ + {d**2}c³ − {3*d}abc',
        'note': ('Кубические расширения — это сложный случай. В общем виде '
                 'не факториальны (если d содержит квадраты простых вида p² — как 18). '
                 'Для конкретных d нужна отдельная проверка. '
                 'Список евклидовых кубических полей: d = 2, 3 и ещё несколько; '
                 'большинство — нет.'),
        'in_database': False
    }


